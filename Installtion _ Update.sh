
## Installtion & Update
# ======================
# Installation
# =============
sudo apt-get install
# Update
# =======
sudo apt-get Update
# updates the package lists for upgrades for packages that need upgrading, as well as new packages that have    
# just come to the repositories. 


# Installation Example "for most important programs"
# ===================================================
# Installing "tree" package
# ==========================
sudo apt-get install tree
# Installing PostgreSQL
# ======================
sudo apt-get update
sudo apt-get install postgresql postgresql-contrib # -contrib package used to install additional utilities
sudo apt-get install postgresql postgresql-client postgresql-contrib # installing postgres with postgres client & contrib package

# Installing PgAdmin
# ===================
sudo apt-get install pgadmin3
# or
sudo apt-get update
sudo apt-get upgrade
sudo apt-get install postgresql-9.3 pgadmin3



