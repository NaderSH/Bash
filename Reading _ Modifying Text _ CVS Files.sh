
### Reading & Modifying Text & CVS Files
## Using Cat Command
# Creating a new file
cat > test.txt #then write in the file
# Saving the file
ctrl+D
# Exit the file
ctrl+C
# Reading the file
cat test.txt

## Using Vi Command
# Creating a new file
vi test2.txt #this will create the file, open it in command mode
# Writing in the file
i #to switch to insert mode
esc #to return to command mode
# Saving the file
:w
# Exiting the file
:q
# Saving & Exiting the file
:wq





